<h3>III) Le phénomène</h3>

<p>Vocaloid est donc un logiciel, mais aussi un phénomène mondial : Tout 
le monde peut créer avec Vocaloid, que cela soit au niveau des paroles des 
chansons, la composition, les illustrations, les PV (équivalent des clips), 
le cosplay, les remixs, les danses, les covers (reprises)...</p>

<p>Depuis le Vocaloid-boom qu’a créé Hatsune Miku, on ne compte plus le 
nombre de chansons postées sur Nico Nico Douga (site équivalant à Youtube, 
très populaire au Japon) et les adaptations ; nous pouvons prendre pour 
preuve le fait que le studio Ghibli serais intéressé de faire une 
adaptation en film d’une trilogie de chansons sous forme de 
long-métrage (Evil’s theatre par Mothy-p (/Akuno-p)), et les 
concerts officiels et amateurs partout dans le monde.</p>

<p>Chaque création est reprise et même améliorée, c’est un travail de groupe. Les compositeurs ayant reçu le titre honorifique de « –p » (producer) sont souvent devenus assez réputés pour la qualité de leur productions et ont même signé avec des labels connus (Le groupe Supercell a signé avec Sony Music Japan). De plus, ces mêmes « -p » ont vu leurs chansons éditées en CDs et être mises dans des jeux, comme le célèbre jeu de rythme Project Diva créé par SEGA et Crypton Future Media, dont il existe plusieurs versions, sur différentes consoles, smartphones et même une version Arcade qui bénéficie régulièrement d’améliorations.</p>

<p>Après le succès de ces jeux, la diva s’est vue propulsée au-devant de la scène et a joué avec des musiciens devant des milliers de spectateurs au Japon, mais aussi à Los Angeles en 2011, à Singapour, et plus récemment Taïwan et Hong-Kong. Lady Gaga s'est également intéressée à Miku puisqu'elle l'a placée en 1ère partie de son dernier concert en 2014.</p>

