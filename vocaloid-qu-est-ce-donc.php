<h3>II) Un peu d'histoire...</h3>

<p>Les premiers Vocaloid étaient destinés à un marché anglophone en 2004. 
Ces Vocaloid n’avaient pas d’apparence fictive, et la première version 
du logiciel de Yamaha laissait à désirer alors que ses concurrents utilisaient 
une technologie plus avancée (comme Cantor par exemple). Leon, Lola et Miriam 
furent donc les premiers Vocaloid, créés par Zero-G.</p>

<p>Quelques mois après la sortie de ces derniers, Meiko (créée par 
Crypton future Media pour le marché Japonais) vit le jour et remportât un 
peu plus de succès que ses prédécesseurs. Cela s’explique par le fait 
qu’elle ait été dotée d’un avatar, et donc d’une apparence fictive lui 
conférant un charme absent des présent Vocaloid "sans visage". On peut 
également souligner son timbre de voix plus réaliste.</p>

<p>Dès lors, la recherche de réalisme, autant dans la voix que dans 
l’identité du personnage (physique et même caractère forgé par les fans 
au fil du temps) sera la clé du succès des logiciels Vocaloid: 
CFM (Crypton Future Media), après avoir vendu plus de 3 000 exemplaires 
de Meiko, décida de créer un nouveau Vocaloid en 2006, Kaito. Contrairement à son aînée
celui ci ne bénéficia pas d’une grande popularité 
auprès des utilisateurs et se vendit seulement à 500 copies.</p>

<p>Cet échec contraindra les développeurs à ne plus créer de Vocaloid 
masculins, peu demandés à l’époque (contrairement à aujourd’hui où Kaito est un 
des Vocaloid masculins les plus populaires).</p>

</div>
</div>

