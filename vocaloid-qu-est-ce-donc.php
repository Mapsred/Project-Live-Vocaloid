<p>La réputation du logiciel s’est même étendue jusqu’en Amérique, 
Europe et l’Asie, nous en avons pour preuve la rediffusion en différé 
du Mikupa Sapporo 2011 dans une salle française au Méga CGR de Torcy, 
un des avatars Vocaloid (Megpoid Gumi) est devenue la mascotte de 
l’émission Japan In Motion sur la chaine Nolife, et la sortie de 
l’album Supercell feat. Hatsune Miku dans les bacs, édités par 
Kaze/wasabi records.</p>

<p>De plus, les Vocaloid de Crypton ont été récemment placés sous 
les Creative Commons, ce qui permet à n’importe-qui de créer avec 
les Vocaloid de cette société. Voici comment un simple travail 
amateur peut devenir un succès mondial.</p>

