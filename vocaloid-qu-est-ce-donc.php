<h3>I) Le logiciel</h3>

<p>Vocaloid est un logiciel de synthèse vocale sous la forme d’un logiciel 
MAO (Musique Assistée par Ordinateur). Les voix (voice bank) sont 
enregistrées avec des voix de chanteurs ou de comédiens de doublage 
(seiyus au Japon), puis après l’échantillonnage de la voix, elle est 
ensuite travaillée grâce à la technologie de Yamaha.</p>

<p> Après portage dans 
le logiciel, il est possible de former un chant avec paroles, différents 
timbres de voix ou autres effets pour rendre le chant naturel. Quand la 
voix est prête, parfois un design de personnage est associé à la voix pour 
rendre le logiciel plus sympathique.</p>

<p> Yamaha n’a pas le monopole de création 
de personnages Vocaloid, bien d’autres sociétés ont créé elles aussi 
des Vocaloids (Voice Bank + personnage utilisant la technologie de Yamaha) 
comme par exemple Crypton Future Media, inc., AH-Software, Internet-CO, 
Zero-G Limited,… La liste de créateurs de Vocaloid est longue. </p>

<p> L’utilisation du logiciel en lui-même est simple, mais la complexité 
vient des voix. Par exemple, la voix de Hatsune Miku est beaucoup plus 
simple à travailler que celle de Megurine Luka, car Luka a été créé 
pour des utilisateurs plus expérimentés. </p>

<p> Pour créer une chanson, 
l’utilisateur as devant lui une interface représentant un clavier 
de piano, ainsi il peut intégrer des notes sur différentes hauteurs, 
ou charger un fichier MIDI, ou VSQ qui est le format spécifique 
à Vocaloid (VSQX pour Vocaloid 3). L’utilisateur peut ensuite 
entrer des paroles et syllabes et modifier différents paramètres 
pour jouer avec la voix et la rendre plus naturelle ou même plus 
robotique, de même, des respirations peuvent être ajoutées ainsi 
que des « phonèmes cachés ».</p>

<p> Aujourd’hui, il existe une soixantaine 
de Vocaloid chantant différentes langues (Japonais, Anglais, Coréen, 
Espagnol, Chinois,…) et en français, grâce au 
projet CeviO ALYS de VoxWave !</p>

