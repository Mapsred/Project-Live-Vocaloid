<div id="text_p_4">

<p>Durant l’été 2007, une seconde version du logiciel vit le jour, qu'on appela tout simplement VOCALOID 2,
pour marquer une "seconde génération". Après quelques mois de développement, le 31 Août, Hatsune Miku naquit.</p>

<p>Alors que 1000 exemplaires de vente étaient considérés à cette époque comme une 
réussite pour un Vocaloid, Hatsune Miku se vendit à plus de 40 000 copies !
La raison de ce succès ? Son design frais (jeune fille de 16 ans aux 
couettes turquoises), sa voix adolescente et les dernières technologies 
de Yamaha pour son logiciel (supérieur à ses concurrents à ce moment-là); qui 
séduisit beaucoup de compositeurs amateurs ou professionnels, en plus 
des artistes et autres personnes participant au phénomène encore 
aujourd’hui.</p>

<p> Cette pétillante jeune fille de 16 ans devint donc très rapidement un 
phénomène social d’une ampleur considérable au Japon. Elle apparut 
même dans une série de publicités pour la nouvelle Toyota Corolla, 
et de même une tenue pour un opéra Vocaloid (décembre 2012 au japon, 
15 novembre 2013 en France) lui a été créée par Marc Jacobs, 
un des créateurs travaillant chez Louis Vuitton.</p>

</div>
</div>

